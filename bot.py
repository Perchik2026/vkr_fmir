# ========== РАБОТА С ДАННЫМИ ==========
def load_user_data():
    try:
        with open(USER_DATA_FILE, 'r', encoding='utf-8') as f:
            return json.load(f)
    except:
        return {}

def save_user_data(data):
    with open(USER_DATA_FILE, 'w', encoding='utf-8') as f:
        json.dump(data, f, ensure_ascii=False, indent=2)

def update_user(user_id, first_name, last_name, action):
    data = load_user_data()
    uid = str(user_id)
    now = datetime.now().isoformat()
    if uid not in data:
        data[uid] = {
            "user_id": user_id,
            "first_name": first_name,
            "last_name": last_name,
            "created_at": now,
            "last_activity": now,
            "actions": []
        }
    data[uid]["last_activity"] = now
    data[uid]["actions"].append({"action": action, "time": now})
    save_user_data(data)

# ========== ОТПРАВКА СООБЩЕНИЙ ==========
def send_message(user_id, text, keyboard=None):
    url = "https://api.vk.com/method/messages.send"
    payload = {
        "user_id": user_id,
        "message": text,
        "random_id": int(time.time() * 1000),
        "access_token": VK_TOKEN,
        "v": "5.199"
    }
    if keyboard:
        payload["keyboard"] = json.dumps(keyboard, ensure_ascii=False)
    requests.post(url, data=payload)

def make_keyboard(buttons, one_time=False):
    keyboard = {"one_time": one_time, "buttons": []}
    for row in buttons:
        keyboard["buttons"].append([{
            "action": {"type": "text", "label": btn},
            "color": "primary"
        } for btn in row])
    return keyboard

