def handle_message(user_id, text, first_name, last_name):
    update_user(user_id, first_name, last_name, text[:50])

    if text == "🔙 Главное меню" or text.lower() in ["start", "начать", "/start"]:
        send_message(user_id, f"🎓 Добро пожаловать, {first_name}!\n\nВыберите раздел:", main_keyboard())
        return

    if text == "📋 ОТЧЕТ О ПП":
        send_message(user_id, "Выберите раздел ПП:", pp_keyboard())
        return

    if text == "📝 ОБЩИЕ ТРЕБОВАНИЯ (в разработке)":
        send_message(user_id, "📝 Раздел ОБЩИЕ ТРЕБОВАНИЯ находится в разработке", main_keyboard())
        return

    if text == "📚 ВКР (в разработке)":
        send_message(user_id, "📚 Раздел ВКР находится в разработке", main_keyboard())
        return

    if text == "📄 ПОЛЬЗОВАТЕЛЬСКОЕ СОГЛАШЕНИЕ":
        send_message(user_id, "📄 Пользовательское соглашение в разработке.", main_keyboard())
        return

    if text in PP:
        send_message(user_id, PP[text], pp_keyboard())
        return

    if text in REQ:
        send_message(user_id, REQ[text], req_keyboard())
        return

    send_message(user_id, "❌ Неизвестная команда. Используйте кнопки или напишите /start", main_keyboard())

# ========== LONG POLL ==========
def get_longpoll_server():
    url = "https://api.vk.com/method/groups.getLongPollServer"
    params = {
        "group_id": GROUP_ID,
        "access_token": VK_TOKEN,
        "v": "5.199"
    }
    r = requests.get(url, params=params).json()
    if "error" in r:
        raise Exception(f"VK API error: {r['error']}")
    return r["response"]["server"], r["response"]["key"], r["response"]["ts"]

def run_bot():
    try:
        server, key, ts = get_longpoll_server()
        logger.info("Бот запущен")
    except Exception as e:
        logger.error(f"Ошибка получения LongPoll сервера: {e}")
        return

    while True:
        url = f"{server}?act=a_check&key={key}&ts={ts}&wait=25"
        try:
            r = requests.get(url, timeout=30).json()
            ts = r.get("ts", ts)
            for update in r.get("updates", []):
                if update.get("type") == "message_new":
                    msg = update["object"]["message"]
                    if msg.get("from_id") and (msg.get("peer_id") == msg["from_id"]):
                        user_id = msg["from_id"]
                        text = msg.get("text", "")
                        first_name = msg.get("from_first_name", "")
                        last_name = msg.get("from_last_name", "")
                        handle_message(user_id, text, first_name, last_name)
        except Exception as e:
            logger.error(f"LongPoll error: {e}")
            time.sleep(5)
            try:
                server, key, ts = get_longpoll_server()
            except Exception as e2:
                logger.error(f"Ошибка переподключения: {e2}")
                time.sleep(10)

if __name__ == "__main__":
    run_bot()
