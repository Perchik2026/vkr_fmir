# Файл для хранения данных пользователей
USER_DATA_FILE = 'user_data.json'

# ========== РАБОТА С ДАННЫМИ ПОЛЬЗОВАТЕЛЕЙ ==========

def load_user_data():
    try:
        with open(USER_DATA_FILE, 'r', encoding='utf-8') as f:
            return json.load(f)
    except (FileNotFoundError, json.JSONDecodeError) as e:
        logger.warning(f"Ошибка загрузки данных: {e}")
        return {}

def save_user_data(data):
    try:
        with open(USER_DATA_FILE, 'w', encoding='utf-8') as f:
            json.dump(data, f, ensure_ascii=False, indent=2)
    except Exception as e:
        logger.error(f"Ошибка сохранения данных: {e}")

def update_user_data(user_id: int, user_info: dict, action: str = None):
    try:
        user_data = load_user_data()
        user_id_str = str(user_id)
        current_time = datetime.now().isoformat()

        if user_id_str not in user_data:
            user_data[user_id_str] = {
                "user_id": user_id,
                "first_name": user_info.get("first_name", ""),
                "last_name": user_info.get("last_name", ""),
                "created_at": current_time,
                "last_activity": current_time,
                "total_actions": 0,
                "actions_history": []
            }
        else:
            user_data[user_id_str]["last_activity"] = current_time

        if action:
            user_data[user_id_str]["total_actions"] += 1
            user_data[user_id_str]["actions_history"].append({
                "action": action,
                "timestamp": current_time
            })
            user_data[user_id_str]["actions_history"] = user_data[user_id_str]["actions_history"][-50:]

        save_user_data(user_data)
    except Exception as e:
        logger.error(f"Ошибка обновления данных пользователя: {e}")

